			<h3>Inicia REINTECH campaña de recolección de Residuos Sólidos Urbanos</h3>				
			<img src="<?php echo base_url(); ?>img/principal.jpg" alt="" align="center" width="350">
			<p>A fin de reducir el impacto ambiental negativo, evitar la acumulación en los hogares de Residuos sólidos urbanos inorgánicos los cuales tienen un efecto desfavorable para el medio ambiente, la empresa REINTECH, Recycled, Innovation and Technology inició una campaña en la que invita a los ciudadanos a donar este tipo de residuos.</p>
			<p>REINTECH que es una empresa dedicada al desarrollo de investigación, diseño y producción de tecnologías verdes, fomentando el uso, conservación y reutilización de materiales renovables y aprovechamiento de residuos se ha visto en todo momento interesada en fomentar este tipo de campañas para favorecer no solo al medio ambiente sino a la ciudadanía quien también se ve afectada al mantener mucho tiempo almacenados en su casa estos residuos.</p>
			<p>La campaña que se lleva a cabo en la capital del estado de Veracruz tiene como objetivo estar en sintonía con el consumo acelerado que  propicia una gran cantidad de residuos sólidos urbanos inorgánicos quienes deben también de tener un destino adecuado ya sea para su reciclaje, reciclaje o correcta recanalización.</p>
			<p>“Se debe atender estos tema con toda la relevancia y cuidado que esto merece, y este tipo de eventos resalta esa importancia, ocupación y preocupación que tenemos por nuestro medio ambiente”. Comentario que sobresalió por parte de uno de los participantes.</p>
			<p>Este es un proyecto que crece con el esfuerzo en conjunto del gobierno, sociedad civil organizada y la empresa recicladora, y tiene la visión de limpiar a Xalapa y zonas aledañas de Residuos Sólidos Urbanos Inorgánicos.</p>
			<p>REINTECH agregó que los residuos que acepta son: plásticos, cartón, vidrio y metales, así como algunos Residuos de aparatos eléctricos y electrónicos (RAEE).</p>
			<p>“Sin el apoyo de la ciudadanía este tipo de campañas no tendrían lugar ni relevancia, por esa razón invitamos a una concientización social para ayudarnos cuidando el medio ambiente”, comentario que resaltó por el organizador de la campaña.</p>
