			<h3>Se concluyen cuatro estudios de composición de RSU en los municipios de Xalapa, Perote, Coatepec y Banderilla</h3>						
		<p>Como parte de las actividades del proyecto denominado “DESARROLLO DE UN MODELO DE INNOVACIÓN TECNOLÓGICA PARA EL  MANEJO INTEGRAL DE RESIDUOS SÓLIDOS URBANOS EN LA REGIÓN CAPITAL DEL ESTADO DE VERACRUZ” la empresa REINTECH realizó cuatro caracterizaciones en los municipios de Xalapa, Perote, Coatepec y Banderilla.</p>		
		<img src="<?php echo base_url(); ?>img/sec1.jpg" alt="" align="center" width="350">
		<p>Estas actividades comprendieron inicialmente con entrevistas con las personas encargadas de limpia pública en los municipios y finalmente con visitas a los sitios de disposición final, en los cuales se realizaron caracterizaciones de RSU, basándose en las normas NMX-AA-15-1985, NMX-AA-19-1985 y NMX-AA-22-1985, lo cual permitió tener una mejor obtención de datos. Las caracterizaciones se llevaron a cabo durante dos días en cada municipio en el cual, aleatoriamente se seleccionaron camiones para su muestreo y posterior separación en metales, papel, orgánicos, etc.</p>
		<img src="<?php echo base_url(); ?>img/sec2.jpg" alt="" align="center" width="350">
