			<h4>Biomímesis</h4>
			<p>(Bio-vida y mimesis- imitar), también conocida como biomimética o biomimetismo, es la ciencia que estudia a la naturaleza como fuente de inspiración,  para tecnologías innovadoras para resolver aquellos problemas humanos que la naturaleza ha resuelto, mediante los modelos de sistemas (mecánicos), procesos (químicos) y elementos que imitan o se inspiran en ella.</p>
			<p>Esta filosofía para el diseño de productos está basada en una metodología que implica <strong>Identificar</strong>  el planteamiento del problema que se pretende resolver, definiendo lo que se busca del producto; el siguiente paso es <strong>traducir</strong> ,  planteando el problema en términos empleados en la naturaleza, a través de analogías; el siguiente paso implica una búsqueda de aquellos sistemas que pudieran dar solución al problema, lo anterior a través de la <strong>observación</strong> ; A través de la abstracción se seleccionan aquellos patrones o procesos que garantizan su funcionalidad y son considerados una alternativa para resolver la problemática; el siguiente paso es la <strong>evaluación</strong>  del diseño a través de la comparación con los <u>principios de la vida</u> , posteriormente se realizarán evaluaciones que permitan <strong>reafirmar</strong>  la selección del sistema y sus posibles mejoras, pero siempre basados en los principios de la vida.</p>
