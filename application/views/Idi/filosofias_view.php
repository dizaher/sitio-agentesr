			<h4>Cradle to Cradle</h4>
			<h5>C2C</h5>
			<p>Es una metodología que promueve la mejora continua del producto a través de cinco categorías de calidad: Salud de materiales, reutilización de materiales, energía renovable, administración del agua y la justicia social.</p>
			<p>Esta metodología consiste en que todos los productos, materiales y componentes deben pasar por un riguroso procedimiento de evaluación. Las fases iniciales de la filosofía comprenden la evaluación de las materias primas en términos de criterios de salud humana y ambiental, así como la evaluación  del proceso de fabricación en cuanto a su potencial de reciclaje, uso de agua y de energía, y responsabilidad social. Para dicha evaluación, el formato deberá contener todos los materiales necesarios para la construcción del producto, así como la materia prima para su fabricación y consumibles como los colorantes, catalizadores, tintes, monómeros residuales, etc.</p>
