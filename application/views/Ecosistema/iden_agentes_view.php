		<h4>Identificación de agentes</h4>
		<p>La gestión de los residuos sólidos urbanos no se debe entender únicamente como la recolección y disposición, sino como un proceso más complejo por el que se logra una disminución de los residuos que llegan a su disposición final de manera tal que se depositen en forma menos peligrosa para el ambiente. Este proceso debe comprender factores técnicos, socio-culturales, administrativos, institucionales, legales y económicos interrelacionados.</p>
		<p>En este caso, el manejo integral de los residuos sólidos municipales se entiende como el manejo conjunto de todos los elementos de limpieza y disposición final. Los elementos son: la generación, el almacenamiento temporal, el barrido, la recolección, la transferencia, el transporte, el tratamiento (composteo, reciclaje, incineración, tratamiento mecánico-biológico, etc.) y la disposición final. El manejo integral es parte de la gestión integral de los residuos sólidos municipales que se puede definir como el conjunto articulado de acciones normativas, operativas, financieras y de planificación que una administración municipal puede desarrollar, basándose en criterios sanitarios, ambientales y económicos para recolectar, tratar y depositar los residuos sólidos de su ciudad.</p>
