													<h4>Cámara de diputados</h4>
													<p>La Cámara de Diputados está formada por 500 representantes de los ciudadanos de la nación. Todos los diputados se eligen a través del sufragio libre, universal y secreto cada tres años, en un sistema de voto paralelo: 300 diputados se eligen por medio del escrutinio uninominal mayoritario o mayoría relativa (llamados diputados uninominales) en cada uno de los distritos de tamaño poblacional similar, mientras que los otros 200 se eligen por medio de la representación proporcional (llamados diputados plurinominales) con listas de partido abiertas a partir de cinco grandes circunscripciones en las que se divide el país. Los diputados no pueden reelegirse para el siguiente periodo inmediato.</p>
